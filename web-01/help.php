<h2>Грудь, талия, бёдра</h2>
<p>Ну тут никак, кроме как с гибким сантиметром. Если нет сил дойти до ларька
союзпечати где они по 50 рублей, измеряйте нерастягивающейся верёвочкой и
отмечайте на ней (узелками или фломастером-ручкой). Потом на столе разложите,
измерите линейкой обычной (продают в канцтоварах). Но лучше дойти купить
сантиметр, пригодится. Они продаются кроме ларьков союзпечати, в швейных
магазинах.</p>
<p> Измеряются обхваты, как обхват дерева - обматываемся вокруг
нужного места гибким сантиметром. Замечаем сколько длины сантимера понадобилось
чтоб нас обхватить (один раз). Обхваты груди, талии и бёдер - это просто
места, в которых нужно будет обматываться.</p>
<p><strong>Обхват груди</strong>. Он у нас разный! Самая узкая грудь когда
мы только что выдохнули. Средняя - когда набрали воздуха. Самая широкая (касается
больше парней) - когда вы пытаетесь "порвать мышцами рубашку на спине", ну
и просто подвигать шкафы и потаскать коробки - когда напрягаются мышцы спины
и груди. Что мне давать? Я бы рекомендовал "грудь с воздухом".
Измеряйте "самое широкое" место в области груди - это и будет нужный размер</p>
<p><strong>Талия</strong>. Это элементарное измерение - просто найдите самое
узкое место между бёдрами и грудью и меряйте.</p>
<p><strong>Бёдра</strong>. По сути - самое широкое место на уровне бёдер. Делается
вот как - сделайте кольцо из сантиметра или верёвочки (пальцами просто пережмите
место скрепления) и попытайтесь его &quot;одеть&quot; как будто это штаны и юбка.
Минимальная ширина кольца, при котором вы в него спокойно и не извиваясь
как змея пролазите, и будет правильный обхват
бёдер.</p>
<h2>Как узнать обхваты груди, талии и бёдер тайно от измеряемого</h2>
<p>Во-первых, можно. Но, во-вторых, будет очень неточно. Есть риск что вещь не подойдёт, но такова цена сюрприза. Идеально если вы сможете запудрить жертве мозги/отвлечь внимание/обмануть и фактически просто измерить его сантиметром, просто скрывая свои истинные намерения (подарить одёжку) - например наплести про конкурс красоты или типа.</p>
<p> Второй по точности
метод - получить на руки его актуальную одежду. Понадобится несколько вариантов
для точности - например футболки и рубашка и кофта для обхвата груди. Несколько
разных брюк для обхватов бёдер (и косвенно - талии).</p>
