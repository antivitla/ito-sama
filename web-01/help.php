<h2>Как узнать свой рост</h2>
<p>Встаёте босиком к стеночке или косяку, книжку или ладонь кладём на голову, 
так чтобы краем до стеночки касалась. Изворачиваемся и отмечаем место касания. Дальше
просто меряем расстояние до пола чем угодо. Это ваш рост. Не парьтесь насчёт строгой горизонтальности книжки,
плюс-минус 2 сантиметра и нормально.</p>
<h2>Как измерить чужой рост тайно от измеряемого.</h2>
<p><strong>Используем свой рост</strong>. Измерьте сначала как-нибудь свой рост. Теперь, улучив подходящий момент, прямо посмотрите на жертву и тайно заметьте место куда падает ваш взгляд - например на макушку если вы выше, или на подбородок если вы ниже. Используйте трюк для горизонтальности взгляда - смотрите как будто на дальний горизонт, сквозь человека.</p>
<p>Начинаем операцию "глаз-алмаз": вы должны
оценить разницу этого места с уровнем глаз человека, продолжая с ним мило чатиться (разводить трепаторию/говорилировать/осуществлять разговоризацию). Этим же вечером, под одеялком с фонариком, на листке бумажки отнимите (или прибавьте) эту разницу к своему росту. Это и будет его рост.</p>
<p><strong>Не используем свой рост</strong>. Просто заметьте на глаз когда он/она пройдёт мимо косяка или
любой стены, где его макушка проходит на уровне стены (можете меточкой пометить). Потом ночью находите это место и измеряете любым способом это место до пола. Это его рост, о чудо.</p>
